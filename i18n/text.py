def translate_report_text(text: str, locale: object) -> str:
    """Translate presentation-only report text; never alter data structures."""
    if normalize_locale(locale) != "pt_BR" or not text:
        return text
    value_tokens = {
        key: _PT_REPLACEMENTS[key]
        for key in (
            "yes", "no", "all", "selected", "warning", "required",
            "enabled", "disabled", "PASS", "PASS WITH WARNINGS", "FAIL",
        )
    }
    lines = []
    for original_line in str(text).splitlines():
        line = original_line
        for source, target in sorted(
            _PT_SENTENCES.items(), key=lambda item: len(item[0]), reverse=True
        ):
            line = line.replace(source, target)
        for source, target in sorted(
            _PT_REPLACEMENTS.items(), key=lambda item: len(item[0]), reverse=True
        ):
            if source in value_tokens:
                continue
            stripped = line.strip()
            if (
                stripped == source
                or stripped.startswith(source + ":")
                or stripped.startswith(source + " (")
            ):
                offset = line.find(source)
                line = line[:offset] + target + line[offset + len(source):]
            elif source in {
                "ADMISSIBLE", "REJECTED", "NOT EVALUATED", "INSUFFICIENT DATA",
                "SIZED WITH WARNING", "SIZED", "NO SOLUTION", "CHANGE", "KEEP",
                "WARNING", "ERROR",
            }:
                line = line.replace(f"[{source}]", f"[{target}]")
            elif len(source.split()) >= 4 or source.startswith("<"):
                line = line.replace(source, target)
        for source, target in sorted(value_tokens.items(), key=lambda item: len(item[0]), reverse=True):
            line = re.sub(
                rf"(?<=:\s){re.escape(source)}(?=\s|$|\))",
                target,
                line,
                flags=re.IGNORECASE,
            )
            line = re.sub(
                rf"(?<=\(){re.escape(source)}(?=\))",
                target,
                line,
                flags=re.IGNORECASE,
            )
        lines.append(line)
    return "\n".join(lines)


